#include <bits/stdc++.h>

using namespace std;

#define TEST_CASES(T)           int T; cin>>T; while(T--)
#define FAST_IO                 ios_base::sync_with_stdio(false), cin.tie(NULL)

int main()
{
    FAST_IO;
    
    TEST_CASES(T) {
        int n, m, i, j;
        cin>>n>>m>>i>>j;

        cout<<"1 1 "<<n<<" "<<m<<"\n";
    }
    
    return 0;
}